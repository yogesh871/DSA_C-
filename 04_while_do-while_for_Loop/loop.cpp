#include <iostream>
using namespace std;

int main()
{

//  while loop
{
int i =1;
while (i <= 10){
    cout << i << endl;
    i++;
}
}

//  do while loop
{
int i = 1;
do{
      cout << i << endl;
    i++;
}
while (i <= 10);
}

// for loop

int i;
for(i = 1; i<=10; i++ ){
    cout << i << endl;
}

return 0;

}